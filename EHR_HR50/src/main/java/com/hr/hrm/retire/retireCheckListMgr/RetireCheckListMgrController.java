	/**
	 * 퇴직자 checkList 관리  프로시저
	 *
	 * @param session
	 * @param request
	 * @param paramMap
	 * @return ModelAndView
	 * @throws Exception
	 */
	@RequestMapping(params="cmd=prcPHrmRetireCheckList", method = RequestMethod.POST )
	public ModelAndView prcPHrmRetireCheckList(
			HttpSession session,  HttpServletRequest request,
			@RequestParam Map<String, Object> paramMap ) throws Exception {
		// comment 시작
		Log.DebugStart();
		paramMap.put("ssnEnterCd",session.getAttribute("ssnEnterCd"));
		paramMap.put("ssnSabun",session.getAttribute("ssnSabun"));
		
		String sabunStr = (String) paramMap.get("sabunStr");
		String dtStr = (String) paramMap.get("dtStr");
		
		Map map  = null;
		String[] sabunArr = sabunStr.split(",");
		String[] dtStrArr = dtStr.split(",");
		
		Map<String, Object> resultMap = new HashMap<String, Object>();
		 
		for(int i=0; i<sabunArr.length; i++) {
			
			paramMap.put("ssnEnterCd",session.getAttribute("ssnEnterCd"));
			paramMap.put("applSeq","");
			paramMap.put("retirdDt",dtStrArr[i]);
			paramMap.put("gubun","20");
			paramMap.put("sabun",sabunArr[i]);
			
			map = retireCheckListMgrService.prcPHrmRetireCheckList(paramMap);	

			Log.Debug("obj : "+map);
			Log.Debug("sqlcode : "+map.get("sqlcode"));
			Log.Debug("sqlerrm : "+map.get("sqlerrm"));
			
			resultMap.put("Code", "1");
			
			if (map.get("sqlcode") != null && !"OK".equals(map.get("sqlcode").toString())) {
				resultMap.put("Code", map.get("sqlcode").toString());
				
				if (map.get("sqlerrm") != null) {
					resultMap.put("Message", map.get("sqlerrm").toString());
				}
			}
			
		}

		// ModelAndView 생성
		ModelAndView mv = new ModelAndView();
		// return 형태 설정
		mv.setViewName("jsonView");
		mv.addObject("Result", resultMap);
		// 그리드에 맵핑 되는 데이터 이면 DATA에 담아서 보냄
		// comment 종료
		Log.DebugEnd();
		return mv;
	}
	
	
